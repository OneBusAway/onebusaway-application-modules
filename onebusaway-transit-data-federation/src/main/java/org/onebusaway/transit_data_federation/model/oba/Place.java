package org.onebusaway.transit_data_federation.model.oba;

import org.onebusaway.container.model.IdentityBean;

/**
 * A place is something with a street address that we find directions to in the
 * "one bus ride away" explore tool.
 * 
 * @author bdferris
 */
public class Place extends IdentityBean<String> {

  private static final long serialVersionUID = 1L;

  private String id;

  private String name;

  private double lat;

  private double lon;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getName() {
    return name;
  }

  @Override
  public String toString() {
    return this.name;
  }

  public double getLat() {
    return lat;
  }

  public void setLat(double lat) {
    this.lat = lat;
  }

  public double getLon() {
    return lon;
  }

  public void setLon(double lon) {
    this.lon = lon;
  }
}
