package org.onebusaway.enterprise.webapp.actions.status.model;

public class StatusItem {
	public enum Status { ALERT, INFO, OK, WARNING, ERROR };
		
	Status status;
	String title;
	String description;
	
	public Status getStatus() {
		return status;
	}

	public void setStatus(Status status) {
		this.status = status;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
	
	// This should be unique among current StatusItems
	// Used to match different HTML elements for client-side events.
	public String getId() {
	  return Integer.toString(title.hashCode());
	}
	
}
